# Inisialisasi TF-IDF Vectorizer
tfidf_vectorizer = TfidfVectorizer()

# Fungsi untuk mendapatkan top N kata-kata yang paling penting untuk seorang pembicara
def get_top_words_tfidf(dataframe, tfidf_vectorizer, pembicara, n=10):
    # Menghitung TF-IDF hanya untuk teks dari pembicara yang sesuai
    tfidf_matrix = tfidf_vectorizer.fit_transform(dataframe[dataframe['Pembicara'] == pembicara]['Cleaned_Text'])
    # Mendapatkan daftar fitur baru
    feature_names = tfidf_vectorizer.get_feature_names_out()
    # Menghitung nilai rata-rata TF-IDF untuk setiap kata
    avg_tfidf = tfidf_matrix.mean(axis=0).A1
    # Mengurutkan indeks kata berdasarkan nilai rata-rata TF-IDF
    sorted_indices = avg_tfidf.argsort()[::-1]
    # Mengambil top N kata-kata
    top_words = [feature_names[idx] for idx in sorted_indices[:n]]
    # Mengambil skor TF-IDF untuk setiap kata
    top_scores = [avg_tfidf[idx] for idx in sorted_indices[:n]]
    return top_words, top_scores

